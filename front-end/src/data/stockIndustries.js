// 获取所有产业类别
export const getAllIndustries = () => {
    const industries = new Set(Object.values(stockIndustries));
    return Array.from(industries).sort();
};

// 获取特定产业的所有股票
export const getStocksByIndustry = (industry) => {
    return Object.entries(stockIndustries)
        .filter(([_, stockIndustry]) => stockIndustry === industry)
        .map(([stockCode]) => stockCode);
};
